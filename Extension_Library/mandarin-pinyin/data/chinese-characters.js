// Unicode ranges for Simplified Chinese detection
export const simplifiedChineseRanges = [
  { start: 0x4E00, end: 0x9FFF },  // CJK Unified Ideographs
  { start: 0x3400, end: 0x4DBF },  // CJK Extension A
  { start: 0x20000, end: 0x2A6DF }, // CJK Extension B
  { start: 0x2A700, end: 0x2B73F }, // CJK Extension C
  { start: 0x2B740, end: 0x2B81F }, // CJK Extension D
];

/**
 * Check if a character is Simplified Chinese
 * @param {string} char - Single character to check
 * @returns {boolean} True if character is Chinese
 */
export function isChineseCharacter(char) {
  const code = char.charCodeAt(0);
  return simplifiedChineseRanges.some(range => code >= range.start && code <= range.end);
}

/**
 * Get pinyin reading for a Chinese character or phrase
 * @param {string} text - Chinese character(s)
 * @returns {string|null} Pinyin reading or null if not found
 */
export function getCharacterPinyin(text) {
  // First try phrase mapping for better accuracy
  if (chinesePhraseMap[text]) {
    return chinesePhraseMap[text];
  }
  
  // Then try single character mapping
  return chineseCharacterMap[text] || null;
}